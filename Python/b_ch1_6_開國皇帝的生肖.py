z = ['鼠','牛','虎','兔','龍','蛇','馬','羊','猴','雞','狗','豬']
皇帝 = ['嬴政','劉邦','司馬炎','楊堅','李淵','趙匡胤','忽必烈','朱元璋','皇太極']
出生 = [-259,-256,236,541,566,927,1215,1328,1592]
for i in range(len(出生)):
  if 出生[i] > 0:
    s = (出生[i] - 4) % 12
    print(皇帝[i],'\t','的生肖:',z[s])          # \t 代表跳位鍵，相當於按Tab鍵
  else:
    s = (出生[i] - 4 + 1) % 12                 # 並沒有西元0年
    print(皇帝[i],'\t','的生肖:',z[s])